public class Ejercicio13 {
    public static void main(String[] args){
        
        /* 13. Escribir un programa en Java que imprima por pantalla los números
        del 5 al 15000. */
        
        System.out.println("13. Numeros del 5 al 15000");
        for (int i = 5; i <= 15000; i++) {
            System.out.println("    Numero: " + i);
        }
    }
    
}
